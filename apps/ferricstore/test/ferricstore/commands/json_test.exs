  use Ferricstore.Commands.JsonTest.Sections.JsonClear

  defp store_with_json(key \\ "doc", json_value) do
    store = MockStore.make()
    json_str = Jason.encode!(json_value)
    raw = :erlang.term_to_binary({:json, json_str})
    store.put.(key, raw, 0)
    store
  end

